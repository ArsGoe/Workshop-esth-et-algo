class Letter {
    constructor(letter) {
        this.letter = letter;
        switch(letter) {
            case 't':
                this.grid = t_second;
                break;
            case 'e':
                this.grid = e_second;
                break;
            case 'x':
                this.grid = x_second;
                break;
            default :
                break;
        }
    }
}

const grid = document.getElementById('grid');

t_ascii = new Letter('t');
e_ascii = new Letter('e');
x_ascii = new Letter('x');

// Ce listener permet de regarder quelles touches sont utilisées par l'utilisateur et d'écrire les lettres en conséquences (ou de ::supprimer:: la dernière)
document.addEventListener('keydown', (e) => {
  if (e.key === 't') {
    const letter = document.createElement('div');
    grid.appendChild(letter);
    t_ascii.grid.forEach(row => {
    const rowDiv = document.createElement('div');

    // Check if any cell in this row is a nested grid
    const hasNestedGrid = row.some(cell => Array.isArray(cell));

    if (!hasNestedGrid) {
        // Simple row of characters, just join and append
        rowDiv.textContent = row.join('');
        letter.appendChild(rowDiv);
    } else {
        // Row contains nested grids, so we need to combine them line by line vertically

        // First, find the height of nested grids (assuming all same height, e.g. 32)
        const nestedHeight = row.find(cell => Array.isArray(cell)).length;

        // For each line of the nested grids
        for (let lineIndex = 0; lineIndex < nestedHeight; lineIndex++) {
            let lineStr = '';

            row.forEach(cell => {
                if (Array.isArray(cell)) {
                    // Append the specific line of this nested grid
                    lineStr += cell[lineIndex].join('');
                } else {
                    // For normal cells, just append the character
                    lineStr += cell;
                }
            });

            const nestedRowDiv = document.createElement('div');
            nestedRowDiv.textContent = lineStr;
            letter.appendChild(nestedRowDiv);
        }
    }
    });
  }
  
  if (e.key === 'e') {
  const letter = document.createElement('div');
  grid.appendChild(letter);

  // e_second is 32x32 grid where each cell is a 32x32 grid
  for (let bigRow = 0; bigRow < e_second.length; bigRow++) {
    for (let subRow = 0; subRow < e_second[bigRow][0].length; subRow++) {
      let line = '';
      for (let bigCol = 0; bigCol < e_second[bigRow].length; bigCol++) {
        const cell = e_second[bigRow][bigCol];
        line += cell[subRow].join('');
      }
      const rowDiv = document.createElement('div');
      rowDiv.textContent = line;
      letter.appendChild(rowDiv);
    }
  }
}
  if (e.key === 'x') {
    const letter = document.createElement('div');
    grid.appendChild(letter);

    for (let bigRow = 0; bigRow < x_second.length; bigRow++) {
        for (let subRow = 0; subRow < x_second[bigRow][0].length; subRow++) {
        let line = '';
        for (let bigCol = 0; bigCol < x_second[bigRow].length; bigCol++) {
            const cell = x_second[bigRow][bigCol];
            line += cell[subRow].join('');
        }
        const rowDiv = document.createElement('div');
        rowDiv.textContent = line;
        letter.appendChild(rowDiv);
        }
    }
  }
  if (e.key === 'Backspace') grid.removeChild(grid.lastChild);;
});
